//////////////////////////////////////////////////////
// CSprite.cpp
//////////////////////////////////////////////////////

#include "sprite.h"

//////////////////////////////////////////////////////
// CSprite()
//////////////////////////////////////////////////////
CSprite::CSprite(int iWidth, int iHeight)
{
	m_iWidth = iWidth;
	m_iHeight = iHeight;
	m_fXPos = 0;
	m_fYPos = 0;
}

//////////////////////////////////////////////////////
// ~CSprite()
//////////////////////////////////////////////////////
CSprite::~CSprite(void)
{
}

//////////////////////////////////////////////////////
// CalculateVertices()
//////////////////////////////////////////////////////
void CSprite::CalculateVertices(void)
{
	CUSTOMVERTEX vertex0 = 
		{m_fXPos, (m_fYPos + m_iHeight), 0.0f, 1.0f, 0.0f, 1.0f};				// LL
	CUSTOMVERTEX vertex1 = 
		{m_fXPos, m_fYPos, 0.0f, 1.0f, 0.0f, 0.0f};								// UL
	CUSTOMVERTEX vertex2 = 
		{(m_fXPos + m_iWidth), (m_fYPos + m_iHeight), 0.0f, 1.0f, 1.0f, 1.0f};	// LR
	CUSTOMVERTEX vertex3 = 
		{(m_fXPos + m_iWidth), m_fYPos, 0.0f, 1.0f, 1.0f, 0.0f};				// UR

	m_Vertices [0] = vertex0;
	m_Vertices [1] = vertex1;
	m_Vertices [2] = vertex2;
	m_Vertices [3] = vertex3;
}

//////////////////////////////////////////////////////
// SetPosition()
//////////////////////////////////////////////////////
void CSprite::SetPosition(float fXPos, float fYPos)
{
	SetXPosition(fXPos);
	SetYPosition(fYPos);
}

//////////////////////////////////////////////////////
// SetXPosition()
//////////////////////////////////////////////////////
void CSprite::SetXPosition(float fXPos)
{
	m_fXPos = fXPos;
}

//////////////////////////////////////////////////////
// GetXPosition()
//////////////////////////////////////////////////////
float CSprite::GetXPosition(void)
{
	return m_fXPos;
}

//////////////////////////////////////////////////////
// SetYPosition()
//////////////////////////////////////////////////////
void CSprite::SetYPosition(float fYPos)
{
	m_fYPos = fYPos;
}

//////////////////////////////////////////////////////
// GetYPosition()
//////////////////////////////////////////////////////
float CSprite::GetYPosition(void)
{
	return m_fYPos;
}

//////////////////////////////////////////////////////
// GetVertex()
//////////////////////////////////////////////////////
CUSTOMVERTEX CSprite::GetVertex(int iNum)
{
	return m_Vertices[iNum];
}

//////////////////////////////////////////////////////
// GetWidth()
//////////////////////////////////////////////////////
int CSprite::GetWidth(void)
{
	return m_iWidth;
}

//////////////////////////////////////////////////////
// GetHeight()
//////////////////////////////////////////////////////
int CSprite::GetHeight(void)
{
	return m_iHeight;
}
